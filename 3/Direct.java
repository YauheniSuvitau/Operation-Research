public class Direct {
	private int vertex;
	private int weight;

	public Direct(int vertex, int weight) {
		this.vertex = vertex;
		this.weight = weight;
	}

	public int getVertex() {
		return vertex;
	}

	public void setVertex() {
		this.vertex = vertex;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight() {
		this.weight = weight;
	}
}